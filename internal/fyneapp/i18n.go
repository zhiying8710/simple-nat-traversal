var detectSystemLocales = locale.GetLocales

func detectLocale() string {
	if locale := resolveSupportedLocale(os.Getenv("SNT_GUI_LOCALE")); locale != "" {
		return locale
	}
	if locales, err := detectSystemLocales(); err == nil {
		if locale := resolveSupportedLocale(locales...); locale != "" {
			return locale
		}
	}
	if locale := resolveSupportedLocale(os.Getenv("LC_ALL"), os.Getenv("LANG"), os.Getenv("LANGUAGE")); locale != "" {
		return locale
	}
	return localeEnglish
}

func resolveSupportedLocale(candidates ...string) string {
	for _, candidate := range candidates {
		switch normalizeLocaleCandidate(candidate) {
		case localeChinese:
			return localeChinese
		case localeEnglish:
			return localeEnglish
		}
	}
	return ""
}

func normalizeLocaleCandidate(value string) string {
	value = strings.TrimSpace(value)
	value = strings.Trim(value, "\"'")
	if value == "" {
		return ""
	}
	if head, _, ok := strings.Cut(value, "."); ok {
		value = head
	}
	value = strings.ReplaceAll(value, "_", "-")
	value = strings.ToLower(value)
	switch {
	case strings.HasPrefix(value, "zh"):
		return localeChinese
	case value != "":
		return localeEnglish
	default:
		return ""
	}
}

func (a *App) localizeErrorText(text string) string {
	trimmed := strings.TrimSpace(text)
	if trimmed == "" {
		return ""
	}

	switch trimmed {
	case "server_url is required":
		return a.t("error_server_url_required")
	case "password is required":
		return a.t("error_password_required")
	case "device_name is required":
		return a.t("error_device_name_required")
	case "client runtime is already active":
		return a.t("error_runtime_already_active")
	case "network session is not ready":
		return a.t("error_network_session_not_ready")
	case "client config missing admin_password":
		return a.t("error_client_config_missing_admin_password")
	case "device_id or device_name is required":
		return a.t("device_name_or_id_required")
	case "admin_listen must bind to localhost/loopback only":
		return a.t("error_admin_listen_loopback")
	case "server_url must be an absolute http/https URL":
		return a.t("error_server_url_absolute")
	case "server_url must use https unless allow_insecure_http is true or the host is loopback":
		return a.t("error_server_url_https")
	case "server_url must use http or https":
		return a.t("error_server_url_scheme")
	}

	if rest, ok := strings.CutPrefix(trimmed, "status endpoint returned "); ok {
		return fmt.Sprintf(a.t("error_status_endpoint"), rest)
	}
	if rest, ok := strings.CutPrefix(trimmed, "server admin request failed: "); ok {
		return fmt.Sprintf(a.t("error_server_admin_request_failed"), a.localizeErrorText(rest))
	}
	if rest, ok := strings.CutPrefix(trimmed, "invalid admin_listen: "); ok {
		return fmt.Sprintf(a.t("error_invalid_admin_listen"), rest)
	}
	if rest, ok := strings.CutPrefix(trimmed, "log_level: "); ok {
		return fmt.Sprintf(a.t("error_log_level_detail"), rest)
	}
	if rest, ok := strings.CutPrefix(trimmed, "identity_private is invalid: "); ok {
		return fmt.Sprintf(a.t("error_identity_private_invalid"), rest)
	}

	return trimmed
}
